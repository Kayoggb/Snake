package ch.bbw.pr;

public class GameBoard {
    public int width;
    public int height;

    public GameBoard(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public void update(){
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
